#!/bin/sh
###############################################################################
# /etc 設定備份腳本
# 功能：
#   - 備份目前整個 /etc 設定資料夾內容到 /root/backup/etc_時間戳目錄
###############################################################################

DATE=$(date +%Y%m%d_%H%M%S)
DIR="/root/backup/etc_$DATE"

# 建立目標備份目錄
mkdir -p "$DIR"

# 備份 /etc 下所有內容
cp -r /etc/* "$DIR/"
